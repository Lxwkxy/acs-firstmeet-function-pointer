#include <stdio.h>

int main() {
    // 1. สร้างตัวแปรปกติ (เหมือนสร้างกล่องเก็บของ)
    int score = 100;

    // 2. ประกาศตัวแปร Pointer (เหมือนสร้างกระดาษเปล่าไว้จดเลขห้อง)
    // สังเกต: เราใส่ * ไว้หน้าชื่อตัวแปร เพื่อบอกคอมพิวเตอร์ว่า "นี่คือ Pointer นะ"
    int *ptr;

    // 3. ใช้ & (Address-of) เพื่อ "ขอที่อยู่" 
    // เราขอที่อยู่ของ score แล้วเอามาจดเก็บไว้ในกระดาษ ptr
    ptr = &score;

    printf("=== สำรวจที่อยู่ (Address) ===\n");
    // ใช้ %p ในการปริ้นท์ที่อยู่ (จะได้เลขฐานสิบหกหน้าตาแปลกๆ)
    printf("1. ที่อยู่ของกล่อง score คือ: %p\n", &score);
    printf("2. สิ่งที่จดอยู่ในกระดาษ ptr คือ: %p\n\n", ptr); 
    // สองบรรทัดนี้จะได้เลขเดียวกันเป๊ะ! เพราะ ptr เก็บที่อยู่ของ score ไว้

    printf("=== สำรวจข้อมูลด้วย * (Dereference) ===\n");
    printf("3. ดูคะแนนแบบปกติ (score): %d\n", score);
    
    // 4. ใช้ * นำหน้า Pointer เพื่อ "วาร์ป" ไปตามที่อยู่ที่จดไว้ แล้วเปิดดูของข้างใน
    printf("4. ดูคะแนนผ่าน Pointer (*ptr): %d\n\n", *ptr); 

    // === ไฮไลท์สำคัญ: ลองใช้ Pointer แก้ไขค่า! ===
    // วาร์ปไปที่ห้องนั้น แล้วสั่งเปลี่ยนของข้างในให้เป็น 999
    *ptr = 999; 
    
    printf("=== หลังจากสั่ง *ptr = 999 ===\n");
    printf("5. คะแนนในกล่อง score เปลี่ยนเป็น: %d\n", score); 
    // น้องๆ จะเห็นว่าตัวแปร score โดนเปลี่ยนค่าไปแล้วจริงๆ โดยที่เราไม่ได้แตะคำว่า score เลย!

    return 0;
}